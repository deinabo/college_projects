SELECT SKILL_DESC, SUM(WORKLOG_HOURS), SKILL_RATE, ROUND(SUM(SKILL_RATE * WORKLOG_HOURS), 2) AS 'TOTAL'
FROM SKILL S JOIN TASKSKILL TS ON S.SKILL_ID=TS.SKILL_ID JOIN ASSIGNMENT A ON A.TASKSKILL_ID=TS.TASKSKILL_ID JOIN WORKLOG W ON W.ASSIGN_ID=A.ASSIGN_ID 
WHERE SKILL_DESC <> 'JAVA I' AND SKILL_DESC <> 'JAVA II'
    AND  SKILL_DESC<> 'TECHNICAL WRITER' AND BILL_ID= '100' 
GROUP BY  SKILL_DESC, SKILL_RATE
ORDER BY SKILL_DESC;
/*--*/

