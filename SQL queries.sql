SELECT T.TASK_ID, TASK_DESC, TASK_STARTDATE, TASK_ENDDATE, SKILL_DESC, EMP_LNAME, EMP_FNAME, ASSIGN_STARTDATE, ASSIGN_ENDDATE
FROM TASKSKILL TS JOIN SKILL S ON TS.SKILL_ID = S.SKILL_ID JOIN TASK T ON TS.TASK_ID=T.TASK_ID JOIN ASSIGNMENT A ON TS.TASKSKILL_ID=A.TASKSKILL_ID JOIN EMPLOYEE E ON E.EMP_ID = A. EMP_ID
ORDER BY TASK_STARTDATE, TASK_ENDDATE;
