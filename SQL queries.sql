SELECT PROJ_DESC, SUM(WORKLOG_HOURS) AS 'TOTAL HOURS', ROUND(SUM(SKILL_RATE * WORKLOG_HOURS), 0) AS 'TOTAL BILLED'
FROM PROJECT P JOIN BILL B ON P.PROJ_ID=B.PROJ_ID JOIN WORKLOG W ON W.BILL_ID=B.BILL_ID JOIN ASSIGNMENT A ON A.ASSIGN_ID=W.ASSIGN_ID JOIN TASKSKILL TS ON A.TASKSKILL_ID=TS.TASKSKILL_ID JOIN SKILL S ON S.SKILL_ID=TS.SKILL_ID 
GROUP BY PROJ_DESC
/*--*/

