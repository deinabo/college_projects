SELECT EMP_LNAME, EMP_FNAME, WORKLOG_DATE, A.ASSIGN_ID, TASK_DESC, SKILL_DESC, WORKLOG_HOURS, B.BILL_ID
FROM EMPLOYEE E JOIN ASSIGNMENT A ON E.EMP_ID=A.EMP_ID JOIN WORKLOG W ON W.ASSIGN_ID=A.ASSIGN_ID JOIN BILL B ON B.BILL_ID=W.BILL_ID JOIN PROJECT P ON P.PROJ_ID=B.PROJ_ID JOIN TASK T ON T.PROJ_ID=P.PROJ_ID
JOIN TASKSKILL TS ON T.TASK_ID=TS.TASK_ID JOIN SKILL S ON TS.SKILL_ID = S.SKILL_ID 
ORDER BY ASSIGN_ID, WORKLOG_DATE, WORKLOG_HOURS, TASK_DESC, EMP_LNAME, EMP_FNAME;
/*--*/

11
SELECT EMP_LNAME, EMP_FNAME, TASK_DESC, SKILL_DESC, SUM(WORKLOG_HOURS) AS 'Sum Of Worklog Hours', SKILL_RATE, ROUND(SUM(WORKLOG_HOURS *SKILL_RATE), 2) 'TOTAL'
FROM EMPLOYEE E JOIN ASSIGNMENT A ON E.EMP_ID=A.EMP_ID JOIN WORKLOG W ON W.ASSIGN_ID=A.ASSIGN_ID JOIN BILL B ON B.BILL_ID=W.BILL_ID JOIN PROJECT P ON P.PROJ_ID=B.PROJ_ID JOIN TASK T ON T.PROJ_ID=P.PROJ_ID
JOIN TASKSKILL TS ON T.TASK_ID=TS.TASK_ID JOIN SKILL S ON TS.SKILL_ID = S.SKILL_ID 
GROUP BY EMP_LNAME, EMP_FNAME,TASK_DESC,SKILL_DESC,SKILL_RATE, A.ASSIGN_ID
ORDER BY A.ASSIGN_ID;
