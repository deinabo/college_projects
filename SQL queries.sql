FROM REGION R JOIN EMPLOYEE E ON R.REGION_ID=E.REGION_ID JOIN EMPSKILL ES ON ES.EMP_ID=E.EMP_ID JOIN SKILL S ON S.SKILL_ID=ES.SKILL_ID
