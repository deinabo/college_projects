SELECT EMP_LNAME, EMP_FNAME, SKILL_DESC, SKILL_RATE, SUM(WORKLOG_HOURS) AS 'TOTAL HOURS', ROUND(SUM(SKILL_RATE * WORKLOG_HOURS), 2) AS 'TOTAL'
FROM SKILL S JOIN EMPSKILL ES ON S.SKILL_ID=ES.SKILL_ID JOIN EMPLOYEE E ON ES.EMP_ID=E.EMP_ID JOIN ASSIGNMENT A ON A.EMP_ID=E.EMP_ID JOIN WORKLOG W ON W.ASSIGN_ID=A.ASSIGN_ID
GROUP BY EMP_LNAME, EMP_FNAME, SKILL_DESC, SKILL_RATE
ORDER BY EMP_LNAME, EMP_FNAME, SKILL_DESC, SKILL_RATE;
/*--*/

